     public void readSALAS() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) jTableSalas.getModel();
       
        String bloco = txtbloco.getText();
        
        int id_bloco= Integer.parseInt(bloco);
        
        ArraysDAO salas;
          Conexao conexao = new Conexao();
        salas = new ArraysDAO(conexao);
        
        for(Sala sala: salas.arraysSalas(id_bloco)){
            modelo.addRow(new Object[]{ 
               sala.getId_sala(),
                sala.getId_bloco(),
                sala.getCapacidade(),
                sala.getAndar(),
                sala.getDescricao()
                    
                
              
                
                
            });
          
            
            
            
        }
        
    }
    
    
    
    
