    public void locarSala() throws ParseException, SQLException{
        
        String cod_sala = txtCod.getText();
        String matricula =txtMat.getText();
        String horario = txtHorario.getText();
        String data_alocacao = txtData.getText();
        
        long matriculaL = Long.parseLong(matricula);
        int cod_salaInt = Integer.parseInt(cod_sala);
   
        LocationAluno vazia = AlugarC.ocupada(cod_salaInt);
      
        
       if(vazia== null){
             boolean data =AlugarC.locationAluno(matriculaL, cod_salaInt, horario, data_alocacao );
                if(data){
                   
                    JOptionPane.showMessageDialog(null, "alugada com sucesso");
          
                }else{
                    JOptionPane.showMessageDialog(null, "Nao alugada");
            
                }
      }else if(vazia!= null){
           JOptionPane.showMessageDialog(null, "Sala ja ocupada");
           txtCod.setText("");
       }
       
        
        
    }

